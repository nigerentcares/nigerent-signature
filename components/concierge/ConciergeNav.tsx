export default function ConciergeNav() {
  const pathname = usePathname()

  function isActive(href: string) {
    if (href === '/concierge') return pathname === '/concierge'
    return pathname.startsWith(href)
  }

  return (
    <nav className="con-nav" role="navigation" aria-label="Concierge navigation">
      {TABS.map(tab => (
        <Link
          key={tab.href}
          href={tab.href}
          className={`con-nav-tab ${isActive(tab.href) ? 'active' : ''}`}
          aria-label={tab.label}
        >
          <span
            className="con-nav-ico"
            style={{ color: isActive(tab.href) ? '#1fa3a6' : 'rgba(230,222,213,.3)' }}
          >
            {tab.icon}
          </span>
          <span className="con-nav-lbl">{tab.label}</span>
        </Link>
      ))}
    </nav>
  )
}
